// Program to find Harry's age in 2024

public class problem1 {
    public static void main(String[] args) {

        // Declare variables
        int birthYear = 2000;
        int currentYear = 2024;

        // Calculate age
        int age = currentYear - birthYear;

        // Display result
        System.out.println("Harry's age in 2024 is " + age);
    }
}